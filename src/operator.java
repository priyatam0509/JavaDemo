
public class operator {
/*
 * arithmetic
 * bitwise
 * relational
 * logical
 */
	
public static void main (String args[]) {
	int m =6,n=4;
	n +=m;
	
	System.out.println(n);
	n-=m;
	System.out.println(n);
	n/=m;
	System.out.println(n);
	n*=m;
	System.out.println(n);
	n++;
	System.out.println(n);
	
	
}

}
